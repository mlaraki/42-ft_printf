#include "libft.h"

size_t	ft_strlcpy(char *restrict dst, const char *restrict src, size_t dstsize)
{
	unsigned int i;

	i = 0;
	if (!dst)
		return (0);
	if ((unsigned int)dstsize > 1)
	{
		while (src[i] != '\0' && i < ((unsigned int)dstsize - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (ft_strlen(src));
}
