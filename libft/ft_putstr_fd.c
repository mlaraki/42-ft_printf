#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t i;

	if (!s)
		return ;
	i = 0;
	while (i < ft_strlen(s))
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}
